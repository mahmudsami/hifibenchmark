#!/usr/bin/env python3
"""Generate LaTeX longtables of the complete benchmark results, straight from
the result CSVs, so the appendix numbers cannot drift from the data."""
import csv, os

CSV_DIR = "results/csv"
OUT = "paper/appendix_tables.tex"

MAPPER_NAME = {
    "minimap2": r"\texttt{minimap2}",
    "blend": "BLEND",
    "strobeclust": r"\texttt{strobeclust}",
    "mapquik": r"\texttt{mapquik}",
    "synpact": r"\toolname",
}
# Mappers shown in the main per-genome tables (mapquik has its own separate table)
MAIN_MAPPERS = {"minimap2", "blend", "strobeclust", "mapquik", "synpact"}
MAPPER_ORDER = {m: i for i, m in enumerate(
    ["minimap2", "blend", "strobeclust", "mapquik", "synpact"])}
GENOMES = ["arabidopsis", "human", "maize", "rye"]
GENOME_TITLE = {"arabidopsis": "\\emph{Arabidopsis}", "human": "Human",
                "maize": "Maize", "rye": "Rye"}


def f(x, nd=2):
    try:
        return f"{float(x):.{nd}f}"
    except (ValueError, TypeError):
        return x


def load(path):
    with open(path) as fh:
        return list(csv.DictReader(fh))


# ---- simulated results -----------------------------------------------------
sim = load(os.path.join(CSV_DIR, "results.csv"))
by_genome = {g: [] for g in GENOMES}
for r in sim:
    if r["genome"] in by_genome:
        by_genome[r["genome"]].append(r)

lines = []
lines.append("% Auto-generated by scripts/make_appendix_tables.py -- do not edit by hand.\n")

def _longtable_rows(rows, lines):
    """Append data rows to lines (shared by main and mapquik tables)."""
    for r in rows:
        lines.append(
            f"{MAPPER_NAME.get(r['mapper'], r['mapper'])} & "
            f"{f(r['error_pct'],1)} & {int(r['read_length'])//1000} & "
            f"{int(r['mapped'])} & {f(r['accuracy'])} & {f(r['precision'])} & "
            f"{int(r['wrong_chr'])} & {f(r['map_time_s'])} & {f(r['peak_rss_mb'],1)} \\\\")


HEADER_COLS = (r"\toprule Mapper & Err.\ (\%) & Len.\ (kb) & Mapped & Acc.\ (\%) & "
               r"Prec.\ (\%) & W-chr & Time (s) & RSS (MB) \\ \midrule")

# ---- main per-genome tables (minimap2, BLEND, synpact) ----
for g in GENOMES:
    rows = [r for r in by_genome[g] if r["mapper"] in MAIN_MAPPERS]
    rows.sort(key=lambda r: (float(r["error_pct"]), int(r["read_length"]),
                             MAPPER_ORDER.get(r["mapper"], 9)))
    n = by_genome[g][0]["total_reads"] if by_genome[g] else "?"
    lines.append(r"\begin{longtable}{l r r r r r r r r}")
    lines.append(r"\caption{Complete simulated benchmark for %s (%s reads per condition). "
                 r"Acc.\ is over all reads, Prec.\ over placed reads; W-chr is wrong-chromosome "
                 r"calls; time excludes index loading.}\\" % (GENOME_TITLE[g], n))
    lines.append(r"\label{tab:app-%s}\\" % g)
    lines.append(HEADER_COLS)
    lines.append(r"\endfirsthead")
    lines.append(r"\multicolumn{9}{l}{\footnotesize\itshape \tablename~\thetable\ (continued)}\\")
    lines.append(HEADER_COLS)
    lines.append(r"\endhead")
    lines.append(r"\midrule \multicolumn{9}{r}{\footnotesize continued on next page}\\")
    lines.append(r"\endfoot")
    lines.append(r"\bottomrule")
    lines.append(r"\endlastfoot")
    _longtable_rows(rows, lines)
    lines.append(r"\end{longtable}")
    lines.append("")

# ---- real results ----------------------------------------------------------
real = load(os.path.join(CSV_DIR, "results_real.csv"))

READSET_LABEL = {"hifi": "HiFi", "deepconsensus": "DeepConsensus"}

def _real_longtable(rows, label, caption, lines):
    rhead = (r"\toprule Genome & Mapper & Mapped & Acc.\ (\%) & Prec.\ (\%) & "
             r"Map rate (\%) & Time (s) & RSS (MB) \\ \midrule")
    lines.append(r"\begin{longtable}{l l r r r r r r}")
    lines.append(r"\caption{%s}\\" % caption)
    lines.append(r"\label{%s}\\" % label)
    lines.append(rhead)
    lines.append(r"\endfirsthead")
    lines.append(rhead)
    lines.append(r"\endhead")
    lines.append(r"\bottomrule")
    lines.append(r"\endlastfoot")
    cur = None
    for r in rows:
        if cur is not None and r["genome"] != cur:
            lines.append(r"\midrule")
        cur = r["genome"]
        lines.append(
            f"{GENOME_TITLE.get(r['genome'], r['genome'])} & "
            f"{MAPPER_NAME.get(r['mapper'], r['mapper'])} & {int(r['mapped'])} & "
            f"{f(r['accuracy'])} & {f(r['precision'])} & {f(r['mapping_rate'])} & "
            f"{f(r['map_time_s'])} & {f(r['peak_rss_mb'],1)} \\\\")
    lines.append(r"\end{longtable}")
    lines.append("")

# Main real table — HiFi readset
main_real = [r for r in real
             if r["mapper"] in MAIN_MAPPERS and r.get("readset", "hifi") == "hifi"]
main_real.sort(key=lambda r: (GENOMES.index(r["genome"]) if r["genome"] in GENOMES else 9,
                              MAPPER_ORDER.get(r["mapper"], 9)))
_real_longtable(
    main_real, "tab:app-real",
    r"Complete real-data HiFi benchmark, evaluated by consensus of mappers. "
    r"Acc.\ and Prec.\ are computed over reads in the consensus set; time excludes index loading.",
    lines)

# Rye DeepConsensus real table
rye_dc_real = [r for r in real
               if r.get("readset", "hifi") == "deepconsensus" and r["mapper"] in MAIN_MAPPERS]
rye_dc_real.sort(key=lambda r: MAPPER_ORDER.get(r["mapper"], 9))
if rye_dc_real:
    lines.append("% --- rye DeepConsensus real-data ---")
    lines.append("")
    _real_longtable(
        rye_dc_real, "tab:app-real-rye-dc",
        r"Rye DeepConsensus real-data benchmark, evaluated by consensus of mappers. "
        r"Acc.\ and Prec.\ are computed over reads in the consensus set; time excludes index loading.",
        lines)

with open(OUT, "w") as fh:
    fh.write("\n".join(lines))
print("wrote", OUT, "(%d sim rows, %d real rows)" % (len(sim), len(real)))
